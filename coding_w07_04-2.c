// เมื่อป้อนค่าที่อยู่นอกช่วง 1-4 จะแสดงข้อความแจ้งเตือนว่า "Invalid membership level" ช่วยให้ผู้ใช้รับรู้ว่าข้อมูลไม่ถูกต้อง

// สิทธิประโยชน์ในแต่ละระดับถูกแบ่งแยกและอธิบายได้ชัดเจน ทำให้เข้าใจง่ายและลดข้อผิดพลาดในการแสดงผล

// โค้ดทั้งสองแบบอ่านง่ายและดูแลรักษาได้สะดวก สามารถเพิ่มเติมฟีเจอร์ในอนาคตได้ เช่น เพิ่มระดับสมาชิกใหม่ หรือเปลี่ยนแปลงสิทธิประโยชน์

// กรณีใช้ switch-case จะง่ายและกระชับเมื่อจำนวนระดับสมาชิกไม่มาก และค่าที่ตรวจสอบเป็นค่าคงที่ชัดเจน

